    echo "Exercicio para transformar celsius em fahrenheit e em kelvin \n";
    echo "XXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXX \n";
    $celsius = readline("Diga o valor em Celsius: ");
    $fahrenheit = (9 * $celsius + 160) / 5;
    $kelvin = $celsius + 273;
    echo "Com o valor em celsius: " . $celsius . " o valor em fahrenheit é: " . $fahrenheit . " e o valor em kelvin é: " . $kelvin . "!\n";
